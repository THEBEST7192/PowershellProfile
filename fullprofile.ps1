function flash {
		for ($i = 0; $i -lt 5; $i++) {
		(Get-Host).UI.RawUI.BackgroundColor = 'White'
		(Get-Host).UI.RawUI.ForegroundColor = 'Black'
		Clear-Host
		Start-Sleep -Milliseconds 100
		(Get-Host).UI.RawUI.BackgroundColor = 'Black'
		(Get-Host).UI.RawUI.ForegroundColor = 'Green'
		Clear-Host
		Start-Sleep -Milliseconds 100
	}
}





function flash+ {
    $delay = 100
    $colors = @('Red', 'DarkRed', 'Yellow', 'Green', 'Blue', 'DarkMagenta')

    while ($true) {
        foreach ($color in $colors) {
            # Set both background and foreground color
            (Get-Host).UI.RawUI.BackgroundColor = $color
            (Get-Host).UI.RawUI.ForegroundColor = $color
            Clear-Host
            Start-Sleep -Milliseconds $delay
        }
    }
}




